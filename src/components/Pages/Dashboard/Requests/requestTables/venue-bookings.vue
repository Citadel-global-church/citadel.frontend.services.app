function handleRequest() {
  dispatch("approveOrRejectVenue", {
    managerId: userId.value,
    venueLogId: detail.value.id,
    status: type.value === "approve" ? true : false,
  });
}

function perPage({ currentPerPage }) {
  query.pageNumber = 1;
  query.pageSize = currentPerPage;
}

