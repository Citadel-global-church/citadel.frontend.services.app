const total = computed(() => state.venue.totalRequestCount);

// Define a debounce delay (e.g., 500 milliseconds)
const debounceDelay = 800;
const debouncedSearch = debounce((searchValue) => {
  dispatch("getVenueRequests", { ...query, searchParamter: searchValue });
}, debounceDelay);

watch(success, () => {
  if (success.value) {
    dispatch("getVenueRequests", query);
    modalChange.value.closeModal();
    modal.value.closeModal();
    if (type.value === "approve") {
      toast.success("Approve Successfully");
    } else {
      toast.success("Request Rejected");
    }
  }
});
watch(
  () => query.dateValue,
  () => {
    if (query.dateValue.length) {
      query.EndDate = moment(query.dateValue[1]).format(
        "YYYY-MM-DD HH:mm:ss.SSS"
      );
      query.startDate = moment(query.dateValue[0]).format(
        "YYYY-MM-DD HH:mm:ss.SSS"
      );
    } else {
      query.EndDate = "";
      query.startDate = "";
    }
  }
);
watch(
  () => query.searchParameter,
  () => {
    debouncedSearch(query.searchParameter);
  }
);

watch(
  () => [query.pageNumber, query.pageSize, query.EndDate, query.startDate],
  () => {
    dispatch("getVenueRequests", query);
  }
);
