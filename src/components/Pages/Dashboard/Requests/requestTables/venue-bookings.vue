  //     this.detail = value;
  //     this.$refs.reasonModal.openModal();
  //   },
  // },
];
const handleAction = (status) => {
  let newaction = actions;

  if (status !== null) {
    return newaction.filter((i) => i.name == "view");
  }
  // if (status === false) {
  //   return newaction.filter((i) => i.name !== "reject" || i.name !== "approve");
  // }
  return newaction;
};
const options = [
